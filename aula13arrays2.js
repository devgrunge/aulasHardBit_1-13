var frutas = ["maçã" , "banana"]

console.log(frutas)
// Adiciona um a dado como ultima posição do array
frutas.push("laranja")

console.log(frutas)
// Retira a ulima posição do array
frutas.pop()

console.log(frutas)
//Retira a primera posição do array
frutas.shift()

console.log(frutas)
//adiciona um dado na primera posição do array
frutas.unshift("morango")

console.log(frutas)
